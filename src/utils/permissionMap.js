export function isMapName(level, authSign) {
  var first = parseInt(authSign.slice(0))
  let name
  if (level === 1) {
    PermissionList.map(item => {
      if (authSign === item.authSign) {
        name = item.title
      }
    })
  } else if (level === 2) {
    PermissionList[first - 1].children.map(item => {
      if (authSign === item.authSign) { name = item.title }
    })
  } else if (level === 3) {
    PermissionList[first - 1].children.map(items => {
      var laststr = authSign.lastIndexOf('-')
      var second = authSign.substring(0, laststr)
      if (second === items.authSign) {
        items.children.map(item => {
          if (authSign === item.authSign) { name = item.title }
        })
      }
    })
  }
  return name
}
